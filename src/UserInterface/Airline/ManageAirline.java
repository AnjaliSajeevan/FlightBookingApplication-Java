    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel) airlineScheduleTbl.getModel();
        dtm.setRowCount(0);
        
        for(Airliner a : airlinerDirectory.getAirliners())
        {
            Object [] row = new Object[2];
            row[0]=a;
            row[1]=a.getCountry();
            dtm.addRow(row);
        }
    }
