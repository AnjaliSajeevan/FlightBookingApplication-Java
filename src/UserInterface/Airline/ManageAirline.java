    private void viewAirlinerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewAirlinerBtnActionPerformed
        // TODO add your handling code here:

        int selectedRow =  airlineScheduleTbl.getSelectedRow();
        if(selectedRow <0)
        {
            JOptionPane.showMessageDialog(null,"Pleasse select a row","Warning", JOptionPane.WARNING_MESSAGE);

        }

        else
        {
            Airliner a =(Airliner) airlineScheduleTbl.getValueAt(selectedRow,0);
            ViewAirline panel = new ViewAirline(CardSequenceJPanel,airlinerDirectory,a);
            CardSequenceJPanel.add("ViewAirline",panel);
            CardLayout layout =(CardLayout) CardSequenceJPanel.getLayout();
            layout.next(CardSequenceJPanel);

        }
    }//GEN-LAST:event_viewAirlinerBtnActionPerformed

