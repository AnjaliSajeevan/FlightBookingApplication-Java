    private void deleteAirlineBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAirlineBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow =  airlineScheduleTbl.getSelectedRow();
        if(selectedRow >=0)
        {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult= JOptionPane.showConfirmDialog(null,"Would you like to delete airliner details?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                Airliner a = (Airliner)airlineScheduleTbl.getValueAt(selectedRow,0);
                airlinerDirectory.deleteAirline(a);
                populateTable();

            }
            else
            {
                JOptionPane.showMessageDialog(null,"Pleasse select a row","Warning", JOptionPane.WARNING_MESSAGE);

            }
        }
    }//GEN-LAST:event_deleteAirlineBtnActionPerformed

