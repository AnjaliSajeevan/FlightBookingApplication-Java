    private void btnViewAirplaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewAirplaneActionPerformed
        // TODO add your handling code here:

        int selectedRow =  tblAirplane.getSelectedRow();
        if(selectedRow <0)
        {
            JOptionPane.showMessageDialog(null,"Pleasse select a row","Warning", JOptionPane.WARNING_MESSAGE);

        }

        else
        {
            Airplane f =(Airplane) tblAirplane.getValueAt(selectedRow,0);

            ViewAirplane panel = new ViewAirplane(userProcessContainer,f);
            userProcessContainer.add("ViewAirplane",panel);
            CardLayout layout =(CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        }
    }//GEN-LAST:event_btnViewAirplaneActionPerformed

    private void btnDeleteAirplaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAirplaneActionPerformed
        // TODO add your handling code here:
        int selectedRow =  tblAirplane.getSelectedRow();
        if(selectedRow >=0)
        {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult= JOptionPane.showConfirmDialog(null,"Would you like to delete airplane details?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                Airplane f = (Airplane)tblAirplane.getValueAt(selectedRow,0);
                airliner.getAirplaneDirectory().deleteAirplane(f);
                populateTable();

            }
            else
            {
                JOptionPane.showMessageDialog(null,"Pleasse select a row","Warning", JOptionPane.WARNING_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnDeleteAirplaneActionPerformed

