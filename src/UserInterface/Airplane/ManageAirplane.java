public void populateTable()
    {
       
        int rowCount = tblAirplane.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel) tblAirplane.getModel();
        dtm.setRowCount(0);
         
        for(Airplane a : airliner.getAirplaneDirectory().getAirplaneList()){
        {
            Object [] row = new Object[8];
            row[0]=a;     
            row[1]=a.getSeatingCapacity();
            row[2]=a.getEngine();
            row[3]=a.getLength();
            row[4]=a.getHeight();
            row[5]=a.getWingspan();
            row[6]=a.getCabinWidth();
            
            dtm.addRow(row); 
        }
    }
    }
