    public void populateTable()
    {
      
        int rowCount = tblFlight.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel) tblFlight.getModel();
        dtm.setRowCount(0);
        for(Flight f : airliner.getFlightSchedule().getFlightList()){
        {   DateFormat formattime = new SimpleDateFormat("HH:mm");
            Object [] row = new Object[10];
            row[0]=f;     
            row[1]=f.getOrigin();
            row[2]=f.getDestination();
            row[3]=f.getPreferedTime();
            row[4]=f.getArrivalTime();
            row[5]=f.getFlightDuration();
            row[6]=f.getFlightDate();
            row[7]=f.getTicketPrice();
            row[8]=f.getCrewMembers();
            row[9]=f.getAirplane();
            dtm.addRow(row); 
        }
    }
    }
