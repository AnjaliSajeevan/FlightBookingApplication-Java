    private void btnViewFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewFlightActionPerformed
        // TODO add your handling code here:

        int selectedRow =  tblFlight.getSelectedRow();
        if(selectedRow <0)
        {
            JOptionPane.showMessageDialog(null,"Pleasse select a row","Warning", JOptionPane.WARNING_MESSAGE);

        }

        else
        {
            Flight f =(Flight) tblFlight.getValueAt(selectedRow,0);

            ViewFlight panel = new ViewFlight(CardSequenceJPanel,f);
            CardSequenceJPanel.add("ViewFlight",panel);
            CardLayout layout =(CardLayout) CardSequenceJPanel.getLayout();
            layout.next(CardSequenceJPanel);

        }
    }//GEN-LAST:event_btnViewFlightActionPerformed

    private void btnDeleteFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteFlightActionPerformed
        // TODO add your handling code here:
        int selectedRow =  tblFlight.getSelectedRow();
        if(selectedRow >=0)
        {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult= JOptionPane.showConfirmDialog(null,"Would you like to delete flight details?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION)
            {
               Flight f =(Flight)tblFlight.getValueAt(selectedRow,0);
                airliner.getFlightSchedule().deleteFlight(f);
                populateTable();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Pleasse select a row","Warning", JOptionPane.WARNING_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnDeleteFlightActionPerformed


